    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        // Getting name and security question
        String a1=jTextField1.getText(); // stores the username to verify
        try{
                pst=conn.prepareStatement("select * from Account where Username='"+a1+"'"); // it will select the data of the username stored in a1
                rs=pst.executeQuery();
                if(rs.next())
                {
                    jTextField2.setText(rs.getString(2));
                    jTextField3.setText(rs.getString(4));
                    rs.close();
                    pst.close();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Incorrect Username");
                }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

