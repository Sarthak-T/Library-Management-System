    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:// code for create button
        try
        {
            PreparedStatement pst=conn.prepareStatement("Insert into Account(Username, Name, Password, Security_Q, Answer)values(?,?,?,?,?)");
            pst.setString(1,jTextField1.getText());
            pst.setString(2,jTextField2.getText());
            pst.setString(3,jTextField3.getText());
            pst.setString(4, (String) jComboBox1.getSelectedItem());
            pst.setString(5,jTextField4.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null,"New Account Created");// this will create a dailog box
           
            pst.close();
            clearfield(); // this will clear the previous data from fields 
        }
        catch( SQLException x)
        {
            JOptionPane.showMessageDialog(null,x);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

