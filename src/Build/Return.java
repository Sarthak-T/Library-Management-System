    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String a1=jTextField1.getText(); // stores the id of book
        try{
            pst=conn.prepareStatement("select *from issue where Book_ID='"+a1+"'"); // macthes the book id 
            rs=pst.executeQuery();
            if(rs.next()) // if true it will show remaining data
            {
                jTextField2.setText(rs.getString(2));
                jTextField3.setText(rs.getString(3));
                jTextField4.setText(rs.getString(4));
                jTextField5.setText(rs.getString(5));
           }
            else{
                JOptionPane.showMessageDialog(null, "No such book is Issued");
            }
    }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
            pst=conn.prepareStatement("insert into returnbook(Book_ID,Bname,Student_ID,Sname,Date,RDate)values (?,?,?,?,?,?)");
            pst.setString(1,jTextField1.getText());
            pst.setString(2,jTextField2.getText());
            pst.setString(3,jTextField3.getText());
            pst.setString(4,jTextField4.getText());
            pst.setString(5,jTextField5.getText());
            pst.setString(6,jTextField6.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Book Returned");
            delete();
            clear();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

